<p>
    Hesiod attempts to derive the name Τιτῆνες (Titans) from τιταίνω (stretch, strive) (Th 207-). Possibly the above picture is an explanation of this.
</p>
<p>
    In ancient Greece, just as most everywhere, honor and status are age dependent. Siblings of the same sex are ranked according to their age and the first-born has a special place of honor. If this is true of humans, it must be even more important among the gods<a class="ptr">(1)</a>. So in a well-ordered society gods and people would find a husband/wife of their own rank. All this goes without comment in the Theogony although, Kronos being the youngest, his becoming King of the gods is shown as a kind of coup. I will call a relation like Kronos-Rhea an <em>upward</em> relation (from the male p.o.v.) and one like Okeanos-Tethys a <em>downward</em> relation.
</p>
<br>
<h5>unequal human relations</h5>
<p>
    These things do play a part in the human world. For instance the trope 'hero wins the hand of the princess' can be viewed as an upward relation. The king (or his son) are conceptually the 'best men' in the community<a class="ptr">(2)</a>. If there is a problem they cannot solve and the newcomer can, the latter has shown himself to be better and therefore worthy to be king. The princess is his prize, his future kingly status depends on her. Often but not always, the newcomer turns out to be a highborn prince in disguise. Thus proper order is maintained.
</p>
<p>
    The reverse is the case in Cinderella-like stories. The servant-girl is often a highborn princess who has lost her status. This again, to prevent disturbing the class structure.
</p>
<p>
    The range of meaning of the up/down picture extends even further. There may be negative connotations as in rape (upward) and prostitution (downward). But this is not the web page to go deeply into these matters.
</p>
<br>
<h5>the demigods</h5>
<p>
    But back to the gods: all this would not seem very relevant if it were not for the stories about the <em>demi-gods</em> (ἡμίθεοι), the sons and daughters of gods mating with humans (WD 159-60). This important group (found also in the Old Testament) plays a pivotal role in many if not most of the Greek myths. Relations between gods and women, or goddesses and men, are also either upward or downward. Achilles sprouts from an upward marriage, Peleus and the goddess Thetis; the result is a king. Heracles comes from a downward one: Zeus and Alcmene; the result is a servant. I wonder if there is an ancient theory behind this, that these unequal relations are the cause of much trouble in the world.
</p>
<p>
    There is another point about these demigods - they are not there anymore. They were beautiful - they are gone - they are worshipped. See <a class="textlink" title="scapegoat" href="goat.php">scapegoats</a>. Let us speculate a bit further on that. Both in Homer (Il 12.23-) and Hesiod (WD 157-, about the fourth generation who now live on the Isles of the Blessed in a kind op poetic aside, the demigods are looked back upon as glorious but gone. Is is possible that the semi-divine heroes themselves, as a class, are being scapegoated away<a class="ptr">(3)</a>? See also the Hesiodic Catalogue of women, book 5.201- with fragmentary but interesting references to Zeus' plan to get rid of the demigods, threats of war, the necessity for the Ionians to 'choose the sea' and Apollo's role in this.
</p>
<br>
<h5>the Succession Myth</h5>
<p>
    This myth is an obvious borrowing from Hurrian or Babylonian myths. M.L. West prefers an early date for the transmission, Minoan or Mycenean, among other things on the grounds that the story is completely absorbed into Greek tradition: names of the gods and of places are completely hellenized. West asks the question:
</p>
<div class="indent">
    <p>"Is it to be supposed that at the very beginning of the orientalizing period a complete theological myth was taken
        over bodily from some Near Eastern source, translated completely into Greek terms, and immediately retailed by
        Greek poets and the very priests at Delphi?"<a class="ptr">(5)</a></p>
</div>
<p>
    I would say yes, that is exactly what they were doing, not only with this myth but with the whole Olympic religion, the city-state politeia, the Panhellenic revival, the alphabetic script, not to mention the Great Epics. All of it gives an impression of being based on adaption of some Near Eastern  (Phoenician?) know-how of organizing a modern state. To be sure, there must have been religion in post-Mycenean Greece. Important to the people but formless, local, based on traditions about gods, people and places that we still find in the Olympian <em>kosmos</em> but relegated to the lower echelons. The new religion was not traditional, it was consciously created (using preexisting names of gods) for political purposes. These people knew exactly what they were saying and why.
</p>
<br>
<h5>Kronos</h5>
<p>
    So the succession myth is symbolic of the new policy and the struggle to establish this. Unfortunately Hesiod's footnotes to it are lost and it is now very hard to relate it concretely to history (quite likely there is some sort of civil war or power struggle behind all this) or, for instance, to the  Five Ages of Man myth in the Works and Days. In what Age was the separation of Heaven and Earth by Kronos, and when was the regorging of the stone and the gods? Does it even make sense to ask such questions? Some points:
</p>
<ol>
    <li>In the Golden Age the people lived like gods</li>
    <li>This is the time when Kronos ruled in heaven (WD 111).</li>
    <li>but this came to an end. Because of Pandora? (WD 90)</li>
    <li>Then, it looks like they suffered from degeneration, neglecting their duties, turning it into the Silver Age.</li>
    <li>This generation was ended by Zeus (WD 138)</li>
    <li>Then Zeus created a third generation, of bronze, which was violent and terrible and perished by its own hands (hundred-hands?) (WD 152)</li>
    <li>A fourth generation, better and nobler, was created by Zeus: the demigod heroes of Epic.</li>
    <li>The survivors of this group live forever on the Isles of the Blessed.</li>
    <li>This is followed by the current iron generation.</li>
</ol>
<p>
    The Pandora story appears to say that sex came into the world and things went wrong; the silver age shows symptoms of degeneration from a lack of thumos or andreia (manliness) while the next one seems to suffer from an overdose of that. The succession myth sees the creation of sexual desire as the result of the separation of Ouranos and Gaia: from his genitals the goddess Aphrodite is born. The Ages of Man myth describes the effects: a failing of the 'nothing in excess' rule. It seems reasonable to connect the three myths, although the Pandora story takes place in the time of Zeus - it is another allegory about the same subject, not a history lesson. So the question is this: (a) was Kronos king during the golden age, as seems to be the prevalent interpretation, or (b) did the golden age end <em>because Kronos became king</em>, and before that there was 'heaven on earth'. Against (a) is the unexpected attribution of a paradisal period to the reign of Kronos who is otherwise seen in such negative light. Against (b) is the communis opinio and the fact that it does need some searching to reach this interpretation.
</p>
<p>
    If (b) is accepted, then point 6, the evil bronze race, could be interpreted as representing the same real-world (civil) war as the struggle of Zeus and the Titans. That could be the case except this leaves Zeus doing things (5 en 6) before he wins power. I leave it to the reader to decide. Hesiod's WD 169-169d are really puzzling or out of sequence and I choose to ignore these lines.
</p>
